package org.levelup.hometask3.package3;

import org.levelup.hometask3.ReflectionClass;

@ReflectionClass
public class Supermarkt {
    private String name;

    public Supermarkt() {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Supermarkt{" +
                "name='" + name + '\'' +
                '}';
    }
}
